    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String letter = txtLetter.getText().toUpperCase();
        if (letter.length() != 1) {
            JOptionPane.showMessageDialog(Hangman.this, "Please enter a single letter.");
            return;
        }
        char c = letter.charAt(0);
        if (Character.isLetter(c)) {
            guessLetter(c);
        } else {
            JOptionPane.showMessageDialog(Hangman.this, "Please enter a letter.");
        }
        // Clear the text field after each guess
        txtLetter.setText("");

    }//GEN-LAST:event_jButton1ActionPerformed

