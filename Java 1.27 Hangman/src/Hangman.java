    private void startNewGame() {
        // Initialize the game variables
        wrongGuesses = 0;
        guessedLetters = new HashSet<Character>();
        currentWord = "";
        String[] words = {"ARCHAEOPTERYX", "RENDEZVOUZ", "CAFETERIA", "PHILIPPINES", "MALISCIOUS","HALLUCINATION", "MISSISSIPPI", "DESOLATE", "BADLANDS", "WASTELAND", "DESOLATE", "ABNEGATE", "DEVASTATION", "UNINHABITED"};
        currentWord = words[(int) (Math.random() * words.length)];
        // Update the display with underscores for the unknown letters
        StringBuilder sb = new StringBuilder();
        for (int i = 0; i < currentWord.length(); i++) {
            sb.append("_ ");
        }
        lblGuess.setText(sb.toString());
        lblGuess.setHorizontalAlignment(lblGuess.CENTER);
        // Clear any previously displayed hangman images
        clearHangmanImages();
    }
    
