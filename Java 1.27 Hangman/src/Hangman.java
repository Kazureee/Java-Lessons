    private void updateGuessLabel() {
        // Update the guess label with the correctly guessed letters
        StringBuilder sb = new StringBuilder();
        for (int i = 0; i < currentWord.length(); i++) {
            char c = currentWord.charAt(i);
            if (guessedLetters.contains(c)) {
                sb.append(c);
            } else {
                sb.append("_ ");
            }
        }
        lblGuess.setText(sb.toString());
    }
    
    private void checkForGameOver() {
        if (wrongGuesses >= 9) {
            JOptionPane.showMessageDialog(this, "Game over! You lost. The word was \"" + currentWord + "\"");
            startNewGame();
        } else if (!lblGuess.getText().contains("_")) {
            JOptionPane.showMessageDialog(this, "Congratulations! You won.");
            startNewGame();
        }
    }
    
    private void guessLetter(char c) {
    // Check if the letter has already been guessed
    if (guessedLetters.contains(c)) {
        JOptionPane.showMessageDialog(this, "You already guessed that letter.");
        return;
    }
    // Add the guessed letter to the set
    guessedLetters.add(c);
    // Check if the guessed letter is in the word
    if (currentWord.indexOf(c) >= 0) {
        updateGuessLabel();
    } else {
        // The guessed letter is not in the word
        wrongGuesses++;
        showHangmanImage(wrongGuesses);
    }
    // Check for game over
    checkForGameOver();
    }





    
