    private void btnLogin2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLogin2ActionPerformed
    String Username = txtUser2.getText();
    String Password = new String (txtpas.getPassword());
        Thread thread = new Thread (new Runnable() {
            public void run () {
                for (int i = 0; i <=100; i++) {
                    prgloading.setValue(i);
                    prgloading.setString("LOADING PLEASE WAIT... " + Integer.toString(i) + "%");
                    try {
                        Thread.sleep(20);
                    }
                    catch (Exception e) {
                    }
                }
                if (Username.trim().toLowerCase().equals(User) &&
                    (Password.trim().toLowerCase().equals(Password))){
                    MenuForm success = new MenuForm();
                    success.setVisible(true);
                    success.pack();
                    success.setLocationRelativeTo(null);
                    dispose();
                }
                else if (Username.trim().toLowerCase().equals(User) ||
                        (Password.trim().toLowerCase().equals(Pas))){
                    JOptionPane.showMessageDialog(null, ("Please Input your Username and Password"));
                }
                else if ((!Username.trim().toLowerCase().equals(User)) || (!Password.trim().toLowerCase().equals(Pas))){
                    JOptionPane.showMessageDialog(null, ("The Username or Password is Wrong"));
                }
            }       
        });
        thread.start();
    }//GEN-LAST:event_btnLogin2ActionPerformed

