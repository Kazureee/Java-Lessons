    private void txtpasFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtpasFocusGained
        String pass = String.valueOf(txtpas.getPassword());
        if (txtpas.getText().equals("password")){
        if(pass.trim().toLowerCase().equals("password"))
        {
            txtpas.setText(null);
            txtpas.setForeground(Color.black);
            txtpas.setEchoChar('*');
        }
        }
    }//GEN-LAST:event_txtpasFocusGained

    private void txtpasFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtpasFocusLost
        String pass = String.valueOf(txtpas.getPassword());
        if (txtpas.getText().length()==0){
        if(pass.trim().equals("") || 
           pass.trim().toLowerCase().equals("password"))
        {
            txtpas.setText("password");
            txtpas.setEchoChar('\u0000');
            txtpas.setForeground(new Color(204,204,204));
        }
        }
    }//GEN-LAST:event_txtpasFocusLost

