public class Java107ForLoop2Rows {

    public static void main(String[] args) {
        char letter = 'a';
            for (int i = 1; i <= 2; i++) {
                for (int j = 1; j <= 4; j++) {
                    System.out.print(letter + " ");
            }
        System.out.println();
}

    }
    
}
